package cl.skillnest;

public class WhileYDoWhile {

	public static void main(String[] args) {
		/*
		 * While
		 */
		int contador = 1;
		
		System.out.println("Inicio de while");
		while (contador > 0) {
			System.out.println("Dentro de while");
		}
		System.out.println("Fuera de while");
		
				
	}

}
